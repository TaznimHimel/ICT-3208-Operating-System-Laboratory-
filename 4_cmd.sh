(ii) How do you give read, write, and execute permissions to a file or 
directory in Linux? 
sudo chmod +rwx <filename>
Example: sudo chmod +rwx myfile.txt


(iii) How do you remove read, write, and execute permissions from a file 
or directory in Linux? 
sudo chmod -rwx <filename>
Example: sudo chmod -rwx myfile.txt


(iv) How do you delete a file using the rm command in Linux? 
sudo rm <filename>
Example: sudo rm myfile.txt

